#include <iostream>
#include "Calculator.h"

//Calculus functions

double sum(double num1, double num2) {
	return num1 + num2;
}

double difference(double num1, double num2) {
	return num1 - num2;
}

double product(double num1, double num2) {
	return num1 * num2;
}

double quotient(double num1, double num2) {
	return num1 / num2;
}